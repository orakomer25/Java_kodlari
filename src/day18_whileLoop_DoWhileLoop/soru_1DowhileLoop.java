package day18_whileLoop_DoWhileLoop;

public class soru_1DowhileLoop {
    public static void main(String[] args) {
        /*
        Soru 1- ’k’ harfinden ‘t’ harfine kadar harfleri yazdirin
         */
        char harf='k' ;
        do {
            System.out.println(harf);
            harf++;

        }while (harf<='t');
    }
}
